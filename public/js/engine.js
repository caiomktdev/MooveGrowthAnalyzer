export function buildKPIs(modeloKey, ticketKey, budget) {
  const m = MODELOS[modeloKey];
  const t = TICKETS[ticketKey];
  const cplAvg = (m.kpis.cpl[0] + m.kpis.cpl[1]) / 2;
  const leads = Math.round(budget / cplAvg);
  const conversions = Math.round(leads * t.conversionRate);
  const revenue = conversions * t.avgPrice;
  const roas = (revenue / budget).toFixed(2);

  return [
    { label: 'Leads/mês', value: leads.toLocaleString('pt-BR'), trend: `CPL: R$ ${cplAvg.toFixed(0)}` },
    { label: 'Vendas/mês', value: conversions, trend: `${(t.conversionRate * 100).toFixed(1)}% conv.` },
    { label: 'Receita/mês', value: `R$ ${(revenue / 1000).toFixed(1)}k`, trend: `Ticket: R$ ${t.avgPrice}` },
    { label: 'ROAS', value: `${roas}x`, trend: `Meta: ${m.kpis.roas[0]}–${m.kpis.roas[1]}x` }
  ];
}

export function buildProjection(budget, ticketKey, modeloKey) {
  const t = TICKETS[ticketKey];
  const m = MODELOS[modeloKey];
  const cplAvg = (m.kpis.cpl[0] + m.kpis.cpl[1]) / 2;
  const leadsMonth = budget / cplAvg;
  const baseConv = leadsMonth * t.conversionRate;
  return {
    months: ['Mês 1', 'Mês 2', 'Mês 3'],
    sales: [
      Math.round(baseConv * 0.6),
      Math.round(baseConv * 1.0),
      Math.round(baseConv * 1.4)
    ],
    revenue: [
      Math.round(baseConv * 0.6 * t.avgPrice),
      Math.round(baseConv * 1.0 * t.avgPrice),
      Math.round(baseConv * 1.4 * t.avgPrice)
    ]
  };
}

